public class Marks {
	private int s1;
	private int s2;
	private int s3;
	public Marks(int s1,int s2,int s3) {
		this.s1=s1;
		this.s2=s2;
		this.s3=s3;
	}
	public void setS1(int s1) {
		this.s1=s1;
		
	}
	public void setS2(int s2) {
		this.s2=s2;
	}
	public void setS3(int s3) {
		this.s3=s3;
	}
	public int getS1() {
		return this.s1;
	}
	public int getS2() {
		return this.s2;
	}
	public int getS3() {
		return this.s3;
	}

}
